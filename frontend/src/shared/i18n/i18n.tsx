type TranslationValues = Record<string, string | number>;

type I18nContextValue = {
  locale: Locale;
  t: (key: TranslationKey, values?: TranslationValues) => string;
};

function translate(locale: Locale, key: TranslationKey, values: TranslationValues = {}) {
  const template = locale === "zh-CN" ? ZH_MESSAGES[key] : EN_MESSAGES[key];
  return Object.entries(values).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

const I18nContext = createContext<I18nContextValue>({
  locale: "en",
  t: (key, values) => translate("en", key, values),
});

export function detectLocale(languages: readonly string[]): Locale {
  return languages.some((language) => language.toLowerCase().startsWith("zh"))
    ? "zh-CN"
    : "en";
}

function systemLocale(): Locale {
  if (typeof navigator === "undefined") {
    return "en";
  }
  const languages = navigator.languages.length > 0 ? navigator.languages : [navigator.language];
  return detectLocale(languages);
}

export function I18nProvider({
  children,
  locale,
}: {
  children: ReactNode;
  locale?: Locale;
}) {
  const [detectedLocale, setDetectedLocale] = useState<Locale>(() => locale ?? systemLocale());
  const resolvedLocale = locale ?? detectedLocale;

  useEffect(() => {
    if (locale !== undefined) {
      return;
    }
    const handleLanguageChange = () => setDetectedLocale(systemLocale());
    window.addEventListener("languagechange", handleLanguageChange);
    return () => window.removeEventListener("languagechange", handleLanguageChange);
  }, [locale]);

  useEffect(() => {
    document.documentElement.lang = resolvedLocale;
  }, [resolvedLocale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale: resolvedLocale,
      t: (key, values) => translate(resolvedLocale, key, values),
    }),
    [resolvedLocale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}
